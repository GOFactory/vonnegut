%%%-------------------------------------------------------------------
%% @doc vonnegut public API
%% @end
%%%-------------------------------------------------------------------

-module(vonnegut_app).

-behaviour(application).

%% Application callbacks
-export([start/2
        ,stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    init_tables(),
    vonnegut_sup:start_link().


%%--------------------------------------------------------------------
stop(_State) ->
    ok.

%%====================================================================
%% Internal functions
%%====================================================================

init_tables() ->
    vg_log_segments:init_table(),
    vg_topics:init_table().
