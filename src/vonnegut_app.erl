-module(vonnegut_app).

-behaviour(application).

%% Application callbacks
-export([start/2
        ,stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    {ok, _} = ranch:start_listener(vonnegut_listener, 100,
                                   ranch_tcp, [{port, 5555}], vg_handler, []),
    vonnegut_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

